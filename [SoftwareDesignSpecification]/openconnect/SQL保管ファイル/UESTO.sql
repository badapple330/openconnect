
DROP DATABASE IF EXISTS UESTO;

CREATE DATABASE IF NOT EXISTS UESTO;
USE UESTO;

DROP TABLE IF EXISTS GOODS;
DROP TABLE IF EXISTS CUSTOMER;
DROP TABLE IF EXISTS ORDER_INFO;


CREATE TABLE GOODS(
GOODS_ID INT(100) AUTO_INCREMENT,
GOODS_NAME VARCHAR(255) NOT NULL,
STOCK INT(100),
PRICE INT(100),
KCAL INT(100),
PRIMARY KEY(GOODS_ID)
);


CREATE TABLE  CUSTOMER(
ID INT(100) NOT NULL AUTO_INCREMENT,
CUSTOMER_NAME VARCHAR(255) NOT NULL,
USER_ID VARCHAR(255) UNIQUE NOT NULL,
PASS VARCHAR(255) NOT NULL,
MAILAD  VARCHAR(255) NOT NULL,
POSTCODE  VARCHAR(7) NOT NULL,
ADDRESS  VARCHAR(255) NOT NULL,
TELNUMBER VARCHAR(12) NOT NULL,
credit_number varchar(4),
credit_token varchar(255),
cardNumber varchar(50),
cardHolder varchar(255),
expirationDate varchar(20),
securityCode varchar(4),
PRIMARY KEY(ID)
);


drop table if exists temp;
create table temp(
temp_id int primary key auto_increment,
user_id varchar(255),
user_name varchar(255),
password varchar(255),
email varchar(100),
cell_number varchar(15),
postal_code varchar(8),
address varchar(100),
card_number varchar(50),
card_holder varchar(255),
expiration_date varchar(20),
security_code varchar(4),
oauth_id varchar(50),
oauth_name varchar(50),
registration_date datetime
);


CREATE TABLE ORDER_INFO(
ID varchar(100),
GOODS_ID INT(100),
ORDER_COUNT INT(100),
PURCHASEDAY TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


